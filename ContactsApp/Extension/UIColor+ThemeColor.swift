import UIKit

extension UIColor {
  class func lightTeal() -> UIColor{
    return UIColor(red: 148/255, green: 209/255, blue: 190/255, alpha: 1.0)
  }
  
  class func charcoalGrey() -> UIColor{
    return UIColor(red: 58/255, green: 68/255, blue: 84/255, alpha: 1.0)
  }
  
  class func blueyGrey() -> UIColor{
    return UIColor(red: 145/255, green: 173/255, blue: 194/255, alpha: 1.0)
  }
}
